#include <stdio.h>
#include <stdlib.h>

int main()
{
    int x;
    printf("Hello world!\n");
    printf("%d \n",6);///integer.
    printf("%x \n",134);///hexdecimal
    printf("%ld \n",234422);///long integer
    printf("%lf\n",6434343.98);///long float
    printf("%f\n",68.87);///float
    printf("Enter an integer to find it's hex value:\n ");
    scanf(x);
    return 0;
}
